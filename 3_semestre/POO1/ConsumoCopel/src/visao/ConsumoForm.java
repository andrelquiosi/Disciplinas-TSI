    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int linha = tabela_cliente.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente!", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
            return;
        }

        clienteSelecionado = clientes.get(linha);
        try {

            consumoDao.remover(clienteSelecionado.getId_cliente());
            JOptionPane.showMessageDialog(null, "Pessoa excluida!");

            atualizarTabela();

        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btn_excluirActionPerformed


    private void atualizarTabela() {
        try {
            clientes = consumoDao.buscarPeloNome(jtf_pesquisa.getText());
            DefaultTableModel model = (DefaultTableModel) tabela_cliente.getModel();
            model.setNumRows(0);

            for (int i = 0; i < clientes.size(); i++) {
                Cliente cliente = clientes.get(i);

                model.addRow(new Object[]{cliente.getNome(),
                    cliente.getCpf(), cliente.getUnidade_consumidora(),
                    cliente.getData_vencimento(), cliente.getKwh(),
                    cliente.getValor_total()});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

